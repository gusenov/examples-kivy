# Activate the virtual environment. 
# You will have to do this step from the current directory every time you start a new terminal. 
# This sets up the environment so the new kivy_venv Python is used.

# If you are in linux or macOS, instead do:
source kivy_venv/bin/activate

# Your terminal should now preface the path with something like (kivy_venv), 
# indicating that the kivy_venv environment is active.

